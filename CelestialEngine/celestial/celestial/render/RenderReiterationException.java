package celestial.render;

public class RenderReiterationException extends RuntimeException {
	
	private static final long serialVersionUID = -6233618688119189796L;
	
	public RenderReiterationException() {
		super();
	}
	
	public RenderReiterationException(String message) {
		super(message);
	}
	
	public RenderReiterationException(Throwable cause) {
		super(cause);
	}
	
	public RenderReiterationException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
